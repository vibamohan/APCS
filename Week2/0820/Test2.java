public class Test2 {
  public static void main(String[] args) {
    int var1 = 5;
    int var2 = 7;
    System.out.println(var1 + " " + var2);

    float ft1 = 14.2f;
    float ft2 = 778.4f;
    System.out.println(ft1 + " " + ft2);

    char printu = 'u';
    char printe = 'e';
    System.out.println(printu + "" + printe);

    double decimal1 = 2.2;
    double decimal2 = 3.5;
    System.out.println("Decimals: " + decimal1 + ", " + decimal2);

    boolean b1 = true;
    boolean b2 = false;
    System.out.println("Booleans: " + b1 + ", " + b2);

    double decimalSum = decimal1 + decimal2;
    System.out.println("Sum of decimals: " + decimalSum);

    double floatSum = ft1 + ft2;
    System.out.println("Sum of floats: " + floatSum);

    double intSum = var1 + var2;
    System.out.println("Sum of ints: " + intSum);
  }
}
